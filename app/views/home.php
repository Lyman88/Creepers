<h3>Creepers</h3> <p>je konferenční systém, kde registrovaní uživatelé mohou přidávat své strašidelné příběhy k ohodnocení recenzenty.</p>
<br>

<h3>Pokyny pro přispěvatele</h3>
<p>Použitý jazyk může být český i anglický.</p>
<p>Rozsah je omezen na 16 mil. znaků.</p>
<h3>Pokyny pro recenzenty</h3>
<p>Hodnoťte originalitu, téma a kvalitu zpracování příběhu v rozmezí od 1 do 10 (1 nejnižší skóre, 10 nejvyšší).</p>